package nl.duo.team2045.datamodel;

/**
 *  Denotation. (Aanduiding in Dutch).
 *  Use for a field in DutchResidentialAddress. 
 *  Not used often these days. Intended for houseboots and other places that have no housenumber.
 * @author hkruse
 *
 */
public enum Denotation {
	/**
	 * ahead of/across the street. (tegenover in Dutch).
	 */
	TO, 
	/** Near. (bij in Dutch) */
	BY
}
